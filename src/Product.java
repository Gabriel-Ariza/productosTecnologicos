
class Product {
    String name;
    double price;
    int referenceNumber;
    int quantity;
    int category;

    Product(String name, double price, int referenceNumber, int quantity, int category) {
        this.name = name;
        this.price = price;
        this.referenceNumber = referenceNumber;
        this.quantity = quantity;
        this.category = category;
    }
}
